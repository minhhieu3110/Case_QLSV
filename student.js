class Student{
    id;
    name;
    mathScore;
    physicalScore;
    englishScore;
    averageScore;
    ranked;

    constructor(idStu, nameStu, mathStu, physicalStu, englishStu, averageStu, rankStu) {
        this.id = idStu;
        this.name = nameStu;
        this.mathScore = mathStu;
        this.physicalScore = physicalStu;
        this.englishScore = englishStu;
        this.averageScore = averageStu;
        this.ranked = rankStu;
    }

}